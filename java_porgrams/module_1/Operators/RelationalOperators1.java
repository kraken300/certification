public class RelationalOperators1{
	public static void main(String[] args) {
		System.out.println(10>11);	//false
		System.out.println(10<'5');	//true
		System.out.println(10>=11); //false
		System.out.println(10<='5'); //true
		System.out.println(10==11); //false
		System.out.println(10!=11); //true
	}
}